








def ex1():
    x = 5
    print(type(x)) # int

def ex2():
    x = "Hello World"
    print(type(x)) # str
    
def ex3():
    x = 20.5
    print(type(x)) # float

def ex4():
    x = ["apple", "banana", "cherry"]
    print(type(x)) # list

    
def ex5():
    x = ("apple", "banana", "cherry")
    print(type(x)) # tuple

    
def ex6():
    x = {"name" : "John", "age" : 36}
    print(type(x)) # dict

    
def ex7():
    x = True
    print(type(x)) # bool

    


